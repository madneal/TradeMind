def get_industry(code: str, name: str = "") -> str:
    """查股票所属行业。优先代码精确匹配，其次名称关键词匹配。"""
    if code in _INDUSTRY_MAP:
        return _INDUSTRY_MAP[code]
    if name:
        for keyword, industry in _NAME_INDUSTRY_MAP:
            if keyword in name:
                return industry
    return "未知"
